a = 10;
b = 4;
 
# Print bitwise AND operation
print("a & b =", a & b);
print();
 
# Print bitwise OR operation
print("a | b =", a | b);
print();
 
# Print bitwise NOT operation
print("~a =", ~a);
print();
 
# print bitwise XOR operation
print("a ^ b =", a ^ b);
print();

# print bitwise right shift operator
print("a >> 1 =", a >> 1);
print("b >> 1 =", b >> 1);
print();
 
# print bitwise left shift operator
print("a << 1 =", a << 1);
print("b << 1 =", b << 1);
 