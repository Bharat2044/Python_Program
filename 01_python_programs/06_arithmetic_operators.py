a = 5;
b = 3;

print(a, "+", b, "=", (a + b), "\n");

print(a, "-", b, "=", (a - b), "\n");

print(a, "*", b, "=", (a * b), "\n");

print(a, "**", b, "=", (a ** b), "\n");

print(a, "/", b, "=", (a / b), "\n");

print(a, "//", b, "=", (a // b), "\n");

print(a, "%", b, "=", (a % b), "\n");